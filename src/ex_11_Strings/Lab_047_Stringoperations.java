package ex_11_Strings;

public class Lab_047_Stringoperations {
    public static void main(String[] args) {
        String b1="Test";
        System.out.println(b1.toLowerCase());
        System.out.println(b1.length());
        System.out.println(b1.toUpperCase());
        System.out.println(b1.getBytes());
        System.out.println(b1.concat("ing"));
        System.out.println(b1.contains("e"));
        System.out.println(b1.isEmpty());
    }
}
