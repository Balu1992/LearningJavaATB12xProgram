package ex_04_Operators;

public class Lab011_RelationalOperator {
    public static void main(String[] args) {
        //Relational operators are : >, <, >=, <=,==, !=
        int a=20;
        int b=10;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
