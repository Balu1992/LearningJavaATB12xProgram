package ex_04_Operators;

public class Lab021_Increment_Decrement {
    public static void main(String[] args) {
        int a=10;
        int b=++a; //Value is incremented first and then it's assigned to variable.
        System.out.println(a);
        System.out.println(b);

        int a1=10;
        int b1=a1++; //Varibale is assdigned and then it's incremented
        System.out.println(a1);
        System.out.println(b1);


    }
}
