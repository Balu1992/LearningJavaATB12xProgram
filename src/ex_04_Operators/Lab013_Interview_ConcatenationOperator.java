package ex_04_Operators;
// OPERATOR OVERLOADING where + operator behaves differently with different data types
public class Lab013_Interview_ConcatenationOperator {
    public static void main(String[] args) {
        String a="Balaji";
        String b="BS";
        System.out.println(a+b);

        int a1=10;
        int b1=20;
        System.out.println(a1+b1);
    }
}
